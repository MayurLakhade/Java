package CoreJava.fundamentals;

public class ArithmeticOperators {

    /*
     * + :-- addition
     * - :-- subtraction
     * * :-- multiplication
     * / :-- numbers are divided and quotient given as answer.
     * % :-- numbers are divided and reminder given as answer.
     * 
     */
    public static void main(String[] args) 
    {
        int a = 52;
        int b = 2;

        int x = a/b;
        int c = a%b;

        System.out.println("x = "+x +"\nc = "+c);
    }
    
}
