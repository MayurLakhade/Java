package CoreJava.statementsAndLoops;
//for loop is used to perform repetitive task.
/*
 * Syntax: 
 * for(initialization ;  ; )
 * {
 *   task to be repeted
 * }
 */
public class ForLoop1 {
    public static void main(String[] args) 
    {
        for(int i = 1; i<=10; i++)
        {
            System.out.println(i);
        }
    }
    
}
